<h1>DIPS</h1>
<hr>
<p>Dips is a small online Digital Image Process System.</p>
<h2>Introduction</h2>
<p>DIPS has two parts in logical, client side and server side.</p>
<ul>
<li>
<p>With client side, user upload images to server, and browser process these images by javascript.</p>
</li>
<li>
<p>With server side, after user upload, server will process these images by python, and send result to browser.</p>
</li>
</ul>
<p>Any images uploaded will save on server, and clean for a period time.</p>
<h2>Use</h2>
<p>Just upload image, then click operate.</p>
<p>You can save result by simple click it.</p>
<h2>Require</h2>
<p>Chrome or Opera or FireFox or other modern browser.</p>
<p>Client Side need &lt;canvas&gt; tag support.</p>
